// 변수를 선언하고 값을 할당한 후, 기본 연산자(+,-,*,/)를 사용하여 간단한 계산을 수행합니다. 
// 예를 들어, 두 숫자를 더한 결과를 출력하거나 나누기를 사용하여 나머지를 계산해 봅니다. 
// 결과물로 코드와 콘솔 출력 스크린샷을 제출합니다.

const a = 2;
const b = 3;
console.log(`a = ${a}`);
console.log(`b = ${b}`);
console.log(`a + b = ${a + b}`);
console.log(`a - b = ${a - b}`);
console.log(`a * b = ${a * b}`);
console.log(`a / b = ${Math.floor(a / b)}`);
console.log(`a % b = ${a % b}`);